# --- Системные прокси ---
# Windows ставит SOCKS-прокси из IE/реестра в системные настройки
# (так делают v2ray/X-Ray). httpx подхватывает их через getproxies(),
# но не умеет SOCKS без socksio. Чистим, чтобы httpx-клиенты ходили
# напрямую. Если когда-то понадобится прокси — задаём явно через
# HTTPS_PROXY=http://host:port в .env.
for _proxy_var in (
    "HTTP_PROXY", "HTTPS_PROXY", "ALL_PROXY",
    "http_proxy", "https_proxy", "all_proxy",
):
    val = os.environ.get(_proxy_var)
    if val is None:
        continue
    if not val.strip() or val.startswith(("socks4://", "socks4a://", "socks5://", "socks5h://")):
        os.environ.pop(_proxy_var, None)


def _strip_windows_registry_proxy() -> None:
    """В Windows httpx читает прокси из реестра через urllib.getproxies().
    Заглушаем эту функцию, чтобы клиенты ходили напрямую."""
    if os.name != "nt":
        return
    try:
        import urllib.request as _ur
        _ur.getproxies_registry = lambda: {}
        _ur.getproxies = lambda: {}
    except Exception:
        pass


_strip_windows_registry_proxy()

